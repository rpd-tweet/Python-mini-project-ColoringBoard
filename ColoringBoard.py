#--------game page---------
def game():
   root1.destroy()
   global root2
   root2=tk.Tk()   
   root2.title("Find What?")
   root2.geometry("275x350")
   root2.resizable(1,1) 
   start()   
   b=tk.Button(root2,text="Close",height=2,width=4,command=lambda: exit()).grid(column=3,row=7)
   root2.mainloop()
#-------exit--------
